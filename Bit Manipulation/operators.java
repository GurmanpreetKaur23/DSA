import java.util.* ;
public class operators {
    public static void main(String[] args) {
        System.out.println("AND (&) OPERATOR : " + (13 & 7));
        System.out.println("OR (|) OPERATOR : " + (13 | 7));
        System.out.println("XOR (^) OPERATOR : " + (13 ^ 7));
        System.out.println("RIGHT SHIFT (>>) OPERATOR : " + (13 >> 1));
        System.out.println("LEFT SHIFT (<<) OPERATOR : " + (13 << 1));
        System.out.println("NOT (~) OPERATOR : " + (~5));
    }
}
